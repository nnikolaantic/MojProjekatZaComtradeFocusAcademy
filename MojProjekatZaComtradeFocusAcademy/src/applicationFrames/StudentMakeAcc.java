    private boolean proveraUsername() {
    try{
        String usernameIzBaze = txtCreateUsername.getText();
        String queryZaUsername = "SELECT * FROM students_table WHERE username = '" + usernameIzBaze + "'";
        Statement st = DatabaseConnection.getConnection().createStatement();
        ResultSet rs = st.executeQuery(queryZaUsername);
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Username already exist");
                txtCreateUsername.setText("");
                return true;
            }  
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        } 
        return false;
    }

