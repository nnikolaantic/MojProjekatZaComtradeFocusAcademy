    private void btnChooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        if(f == null){
            maleOrFemale(); 
        return;
        }else{
        fileName = f.getAbsolutePath();
        ImageIcon imageIcon = new ImageIcon(new ImageIcon(fileName).getImage()
                .getScaledInstance
                (lbl_img.getWidth(),lbl_img.getHeight(),Image.SCALE_SMOOTH ));
        lbl_img.setIcon(imageIcon); 
        try {
            File image = new File (fileName);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            for (int readNum ; (readNum=fis.read(buf)) != -1;){
                bos.write(buf, 0, readNum);
            }
            person_image = bos.toByteArray();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_btnChooseActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
         if(txtCreateUsername.getText().trim().isEmpty() && txtCreatePassword.getText().trim().isEmpty()){
            jLabelEmptyUsername.setText("Username is Empty");
            jLabelEmptyPassword.setText("Password is Empty");
            return;
        }else if(txtCreateUsername.getText().trim().isEmpty()){
            jLabelEmptyUsername.setText("Username is Empty");
            return;
        }else if(txtCreatePassword.getText().trim().isEmpty()){
            jLabelEmptyPassword.setText("Password is Empty");
            return;
        }else
        
        try{
            if(proveraUsername()){
            return;
            }
            
            String query = "INSERT INTO students_table (firstname,lastname,gender,address,subjects,profesor,images,username,password)VALUES(?,?,?,?,?,?,?,?,?)";
            PreparedStatement pst = DatabaseConnection.getConnection().prepareStatement(query);

//            pst.setString(1, tabela1);
            pst.setString(1, firstname.getText());
            pst.setString(2,lastname.getText());
            if(male.isSelected()){
                gender="Male";
            }
            if(female.isSelected()){
                gender="Female";
            }
            pst.setString(3, gender);
            pst.setString(4, address.getText());
            pst.setString(5, jComboBoxSubjects.getSelectedItem().toString());
            
            int i = jTableProfesori.getSelectedRow();
            TableModel model = jTableProfesori.getModel();
            if(i == -1){
                JOptionPane.showMessageDialog(null,"Molimo, izaberite Profesora");
                return;
            }else
                pst.setString(6,model.getValueAt(i,0).toString());
            
            pst.setBytes(7,person_image);
            pst.setString(8, txtCreateUsername.getText());
            pst.setString(9, txtCreatePassword.getText());
            pst.executeUpdate();
           
            JOptionPane.showMessageDialog(null, "Registered Sucessfully!");
            StudentLF slf = new StudentLF();
            slf.setVisible(true);
            setVisible(false);
                    
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

