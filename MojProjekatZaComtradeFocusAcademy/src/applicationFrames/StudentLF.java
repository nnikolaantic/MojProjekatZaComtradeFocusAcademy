    private void postaviPoslovicu() { 
        try{
            String query = " SELECT MAX(id) as brojPoslovica FROM poslovice";
            Statement st = DatabaseConnection.getConnection().createStatement();
            ResultSet rs = st.executeQuery(query);
            int brojPoslovica = 0;
            
            while(rs.next()){
                brojPoslovica = rs.getInt("brojPoslovica");
            }
            st.close();
            Random random = new Random();
            int value = 1 + random.nextInt(brojPoslovica);
            
            String query1 = "SELECT poslovica,autor FROM poslovice WHERE id = " + value ;
            Statement st1 = DatabaseConnection.getConnection().createStatement();
            //mora da se zatvori konekcija na bazu i st close ukoliko dva puta za redom hocu da pristupim bazi
            ResultSet rs1 = st1.executeQuery(query1);
            
            while(rs1.next()){
                jTextAreaPoslovica.setText(rs1.getString("poslovica")); 
                lblAutor.setText(rs1.getString("autor")); 
            }
            
            st1.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
