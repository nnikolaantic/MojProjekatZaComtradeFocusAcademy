    private void SINGINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SINGINActionPerformed

        if(username.getText().trim().isEmpty() && password.getText().trim().isEmpty()){
            lbl_username.setText("Username is Empty");
            lbl_password.setText("Password is Empty");
        }

        if(username.getText().trim().isEmpty()){
            lbl_username.setText("Username is Empty");
        }else if(password.getText().trim().isEmpty()){
            lbl_password.setText("Password is Empty");
        }
        else{
            try{

                String query = "SELECT * FROM students_table WHERE username = ? AND password = ?";

                PreparedStatement pst = DatabaseConnection.getConnection().prepareStatement(query);
                pst.setString(1, username.getText());
                pst.setString(2, password.getText());

                ResultSet rs = pst.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Username and Password matched");
                    new StudentsMenu(username.getText()).setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Username and Password do not mach");
                    username.setText("");
                    password.setText("");
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_SINGINActionPerformed

